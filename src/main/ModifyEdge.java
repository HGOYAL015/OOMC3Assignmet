    private void SearchEdgeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchEdgeActionPerformed
        String from = (String) fromVertex.getSelectedItem();
        String to = (String) toVertex.getSelectedItem();

        if (from.equals(to)) {
            JOptionPane.showMessageDialog(null, "Please Enter FromVertex and ToVertex Different\n");

        } else {
            try {

                Vertex V = OOM.G.searchVertex(from);
                Vertex V1 = OOM.G.searchVertex(to);
                OOM.G.checkEdge(from, to);
                JOptionPane.showMessageDialog(null, "Edge Not Found");

            } catch (Invalid e) {
                double w = OOM.G.getWeight(from, to);
                JOptionPane.showMessageDialog(null, "Edge Found\nFromVertex: " + from + "\nToVertex: " + to + "\nWeight: " + w);
                fromVertex.setEnabled(false);
                toVertex.setEnabled(false);
                ModifyEdge.setEnabled(true);

            }
        }
    }//GEN-LAST:event_SearchEdgeActionPerformed

