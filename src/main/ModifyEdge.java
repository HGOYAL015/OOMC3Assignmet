    private void ModifyEdgeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifyEdgeActionPerformed
        String from1 = (String) fromVertex.getSelectedItem();
        String to1 = (String) toVertex.getSelectedItem();
        String from = (String) fromVertex1.getSelectedItem();
        String to = (String) toVertex1.getSelectedItem();
        String w = weight.getText();
        if (from.equals(to)) {
            JOptionPane.showMessageDialog(null, "Please Enter FromVertex and ToVertex Different\n");

        } else {
            try {
                double weight = Double.parseDouble(w);
                Vertex V = OOM.G.searchVertex(from);
                Vertex V1 = OOM.G.searchVertex(to);
                if (from.equals(from1) && to.equals(to1)) {
                } else {
                    OOM.G.checkEdge(from, to);
                }
                OOM.G.deleteEdge(from1, to1);
                OOM.G.addEdge(from, to, weight);
                JOptionPane.showMessageDialog(null, "Modification Successfull");
                ModifyEdge.setEnabled(false);
                SearchEdge.setEnabled(true);
                fromVertex.setEnabled(true);
                toVertex.setEnabled(true);
//                this.dispose();

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Please Enter the Correct Weight");
            } catch (Invalid e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }

    }//GEN-LAST:event_ModifyEdgeActionPerformed

